void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){

	if (data_rx == '\n'){
		uart_buff[uart_index] = '\0';
		uart_flag = 1;
	}else{
		uart_buff[uart_index++] = data_rx;
	}

	if (uart_flag == 1){
	  uart_flag = 0;
	  uart_index = 0;
	  UART_CMD_Process(uart_buff);
	}
	HAL_UART_Receive_IT(&huart1, &data_rx, 1);
}

